    private void btnTaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTaoMouseClicked

        ArrayList<CauHoi> de = new ArrayList<>();
        ArrayList<CauHoi> kho = new ArrayList<>();
        ArrayList<CauHoi> De = new ArrayList<>();
        ArrayList<CauHoi> Kho = new ArrayList<>();

        for (CauHoi a : danhSachCauHoi) {
            if (a.getDoKho().equals("Dễ")) {
                de.add(a);
            } else {
                kho.add(a);
            }
        }

        Random rd = new Random();
        int soCauDe = doKho * soCauHoi / 100;
        for (int i = 1; i <= soCauDe; i++) {
            int rdDe = rd.nextInt(de.size());
            CauHoi de1 = de.get(rdDe);
            De.add(de1);
            de.remove(rdDe);
        }
        for (int i = 1; i <= soCauHoi - soCauDe; i++) {
            int rdKho = rd.nextInt(kho.size());
            CauHoi kho1 = kho.get(rdKho);
            Kho.add(kho1);
            kho.remove(rdKho);
        }

        ArrayList<CauHoi> danSachCauHoi_BKT = new ArrayList<>();
        danSachCauHoi_BKT.addAll(De);
        danSachCauHoi_BKT.addAll(Kho);

        tblDS.setModel(new CustomTable_DanhSachCauHoi(danSachCauHoi_BKT));
        TaoBaiKiemTRaUI.danhSachCauHoi = danSachCauHoi_BKT;
    }//GEN-LAST:event_btnTaoMouseClicked

    private void tblDSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDSMouseClicked
        selectedRow = tblDS.getSelectedRow();
        CauHoi p = danhSachCauHoi.get(selectedRow);
        txtND.setText(p.getNoiDung());
        txtA.setText(p.getA());
        txtB.setText(p.getB());
        txtC.setText(p.getC());
        txtD.setText(p.getD());
        if ("Dễ".equals(p.getDoKho())) {
            rdDe.setSelected(true);
        } else {
            rdKho.setSelected(true);
        }

        if (p.getDapAn().equals("A")) {
            rdA.setSelected(true);
        }
        if (p.getDapAn().equals("B")) {
            rdB.setSelected(true);
        }
        if (p.getDapAn().equals("C")) {
            rdC.setSelected(true);
        }
        if (p.getDapAn().equals("D")) {
            rdD.setSelected(true);
        }

    }//GEN-LAST:event_tblDSMouseClicked

