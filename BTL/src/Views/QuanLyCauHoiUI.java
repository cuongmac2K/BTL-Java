    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        if (txtND.getText().equals("") || txtA.getText().equals("")
                || txtB.getText().equals("") || txtC.getText().equals("")
                || txtD.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Bạn chưa điền đầy đủ đáp án", "LỖI", JOptionPane.ERROR_MESSAGE);
        } else {
            String noiDung = txtND.getText();
            String dapAnA = txtA.getText();
            String dapAnB = txtB.getText();
            String dapAnC = txtC.getText();
            String dapAnD = txtD.getText();
            String doKho = buttonGroup_DoKho.getSelection().getActionCommand();
            String dapAnDung = buttonGroup_DapAnDung.getSelection().getActionCommand();

            if (QLCauHoiController.themCauHoi(noiDung, dapAnA, dapAnB, dapAnC, dapAnD, dapAnDung, doKho)) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại");
            }

            hienThi_BangCauHoi_KhoDe();
        }
    }//GEN-LAST:event_btnThemMouseClicked

